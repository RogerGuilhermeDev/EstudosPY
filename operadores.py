# + = adição
# - = subtração
# * = multiplicação
# / = divisão
# // = divisão inteira
# ** = potencia 
# % = resto da divisão 

a = 2 + 3  # Soma
b = 2 - 3  # Subtração
c = 2 * 3  # Multiplicação
d = 2 / 3  # Divisão
e = 2 // 3 # Divisão inteira
f = 2 ** 3 # Potência
g = 2 % 3  # Resto de divisão

print (a, b, c, d, e, f, g)

